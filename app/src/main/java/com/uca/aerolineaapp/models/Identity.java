package com.uca.aerolineaapp.models;

public class Identity {

    private int idIdentity;
    private String passportNumber;
    private String issuePassPortDate;
    private String expPassportDate;
    private String nationality;
    private String birthDate;
    private String identification;
    private String sex;
    private String countryCode;
    private String birthPlace;
    private String registryNumber;

    public int getIdIdentity() {
        return idIdentity;
    }

    public void setIdIdentity(int idIdentity) {
        this.idIdentity = idIdentity;
    }

    public String getPassportNumber() {
        return passportNumber;
    }

    public void setPassportNumber(String passportNumber) {
        this.passportNumber = passportNumber;
    }

    public String getIssuePassPortDate() {
        return issuePassPortDate;
    }

    public void setIssuePassPortDate(String issuePassPortDate) {
        this.issuePassPortDate = issuePassPortDate;
    }

    public String getExpPassportDate() {
        return expPassportDate;
    }

    public void setExpPassportDate(String expPassportDate) {
        this.expPassportDate = expPassportDate;
    }

    public String getNationality() {
        return nationality;
    }

    public void setNationality(String nationality) {
        this.nationality = nationality;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getIdentification() {
        return identification;
    }

    public void setIdentification(String identification) {
        this.identification = identification;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public String getBirthPlace() {
        return birthPlace;
    }

    public void setBirthPlace(String birthPlace) {
        this.birthPlace = birthPlace;
    }

    public String getRegistryNumber() {
        return registryNumber;
    }

    public void setRegistryNumber(String registryNumber) {
        this.registryNumber = registryNumber;
    }
}
